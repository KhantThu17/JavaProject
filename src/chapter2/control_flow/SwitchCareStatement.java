package chapter2.control_flow;

public class SwitchCareStatement {
    public static void main(String[] args) {
        int a = 3;
        switch (a) {
            case 1:
                System.out.println("This is Good");
                break;
            case 2:
                System.out.println("This is Not bad");
                break;
            case 3:
                System.out.println("This is so Good");
                break;
            case 4:
                System.out.println("This is so bad");
                break;
            case 5:
                System.out.println("This is  bad");
                break;
        }
    }
}
